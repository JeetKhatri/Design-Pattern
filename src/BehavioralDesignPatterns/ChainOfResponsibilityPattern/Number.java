package BehavioralDesignPatterns.ChainOfResponsibilityPattern;

public class Number
{
    private int number;
    /***
     * Getter method of Number
     * @param number
     */
    public Number(int number)
    {
        this.number = number;
    }
 
    /***
     * Setter method of Number
     * @return integer
     */
    public int getNumber()
    {
        return number;
    }
 
}
